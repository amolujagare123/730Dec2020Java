public class EvenOddDemo {

    public static void main(String[] args) {
        for(int i=1 ; i<=10 ;i++ )
        {

            if( i % 2 == 0 ) // condition to check the number is even
                System.out.println(i + " Even" );
            else
                System.out.println(i + " odd" );

        }

    }
}
